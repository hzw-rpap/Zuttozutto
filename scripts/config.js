// Terrain Generation Configuration
// 地形生成参数配置文件

export const terrainConfig = {
    // 1. 图像分辨率
    // 降低分辨率可显著提升性能，但会损失细节。
    // 1024x1024 是浏览器端的推荐上限，更高分辨率可能导致卡顿。
    width: 1024,
    height: 1024,

    // 2. 山体高度
    // 控制地形在 3D 世界中的垂直缩放倍数。
    // 值越大，山峰越高耸尖锐；值越小，山势越平缓。
    peakHeight: 300,

    // 3. 侵蝕次數
    // 模拟水流侵蚀的迭代次数。
    // 值越高，沟壑越深，河流侵蚀痕迹越明显。
    erosionCycles: 10000,

    // 4. 模糊次數
    // 用于 Sum_Blurred 算法中的模糊层数。
    // 值越大，地形的大尺度起伏越丰富。通常为 8。
    blurredSteps: 7,

    // 5. 模糊權重
    // 控制不同模糊层级的贡献度。数组长度应与 blurredSteps 一致。
    // 前面的值影响小尺度细节，后面的值影响地形整体结构。
    blurredWeights: [0.2, 0.4, 1.8, 2, 5, 5, 7],

    // 6. 柏林噪聲頻率
    // 控制 Perlin 噪声的叠加层数（Octaves）。
    // 值越高，地形表面的微小杂色和细节越多。推荐 6-8。
    perlinFreq: 15
};
